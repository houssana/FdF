#include <stdlib.h>

int		is_num(char *s)
{
	while (*(s++))
		if (*(s - 1) <= '0' || *(s - 1) <= '0')
			return (0);
	return (1);
}

void	free_tab(char **s)
{
	int i;

	i = -1;
	while (s[++i])
		free(s[i]);
	free(s);
}
